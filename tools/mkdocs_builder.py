class MkDocsBuilder:
    """
    Builds a complete MkDocs site from generated Markdown documentation.
    """

    def __init__(self, docs_dir: str, org_name: str = "Salesforce LOS"):
        self.docs_dir = Path(docs_dir)
        self.site_root = self.docs_dir.parent  # parent of docs/ folder
        self.org_name = org_name
        self.generated_at = datetime.now().strftime("%Y-%m-%d %H:%M")

    def build(self, journey) -> Path:
        """
        Full build: generate mkdocs.yml, assets, index, then run mkdocs build.
        Returns the path to the generated site/ directory.
        """
        logger.info("Building MkDocs site...")

        # 1. Create assets directory
        assets_dir = self.docs_dir / "assets"
        assets_dir.mkdir(exist_ok=True)
        (assets_dir / "mermaid-init.js").write_text(MERMAID_INIT_JS)
        (assets_dir / "extra.css").write_text(EXTRA_CSS)
        logger.info("  Created assets (CSS + JS)")

        # 2. Embed Mermaid diagrams into stage docs
        self._embed_mermaid_diagrams()

        # 3. Generate navigation tree
        nav_section = self._build_nav(journey)

        # 4. Write mkdocs.yml
        mkdocs_yml_path = self.site_root / "mkdocs.yml"
        mkdocs_yml = MKDOCS_YML_TEMPLATE.format(
            generated_at=self.generated_at,
            org_name=self.org_name,
            nav_section=nav_section,
        )
        mkdocs_yml_path.write_text(mkdocs_yml)
        logger.info(f"  Wrote mkdocs.yml → {mkdocs_yml_path}")

        # 5. Write index page
        index_path = self.docs_dir / "index.md"
        index_content = INDEX_MD_TEMPLATE.format(
            org_name=self.org_name,
            generated_at=self.generated_at,
            total_components=journey.total_components,
            stage_count=sum(1 for s in journey.stages if s.components),
            hidden_rules_count=len(journey.hidden_rules_master),
            integration_count=len(journey.integration_inventory),
            risk_count=len(journey.risk_register),
        )
        index_path.write_text(index_content)
        logger.info("  Wrote index.md")

        # 6. Run mkdocs build
        site_path = self._run_mkdocs_build()

        return site_path

    def _embed_mermaid_diagrams(self) -> None:
        """
        Convert standalone .mermaid files into fenced code blocks
        embedded in a new diagram.md file per stage.
        """
        for mermaid_file in self.docs_dir.rglob("*.mermaid"):
            mermaid_content = mermaid_file.read_text()
            stage_name = mermaid_file.parent.name.replace("-", " ").title()

            diagram_md = mermaid_file.parent / "diagram.md"
            diagram_md.write_text(
                f"# {stage_name} — Flow Diagram\n\n"
                f"```mermaid\n{mermaid_content}\n```\n"
            )
            mermaid_file.unlink()  # Remove raw .mermaid file

    def _build_nav(self, journey) -> str:
        """
        Build the nav: section of mkdocs.yml from actual generated files.
        """
        STAGE_SLUG_MAP = {
            "Lead Capture": "01-lead-capture",
            "Application Submission": "02-application",
            "Document Collection": "03-document-collection",
            "KYC & Bureau Verification": "04-kyc-bureau",
            "Financial Analysis & Underwriting": "05-underwriting",
            "Credit Decision": "06-credit-decision",
            "Sanctioning & Offer": "07-sanctioning",
            "Legal Documentation": "08-legal-documentation",
            "Disbursement": "09-disbursement",
            "Post-Disbursal Monitoring": "10-post-disbursal",
            "Collections & Recovery": "11-collections",
            "Loan Closure": "12-loan-closure",
        }

        nav_lines = ["nav:"]
        nav_lines.append("  - Home: index.md")
        nav_lines.append("")

        # Journey section
        nav_lines.append("  - Loan Journey:")
        nav_lines.append("    - Overview: journey/overview.md")

        # Create journey/overview.md as a copy of the main overview
        journey_dir = self.docs_dir / "journey"
        journey_dir.mkdir(exist_ok=True)
        overview_src = self.docs_dir / "00-loan-journey-overview.md"
        if overview_src.exists():
            shutil.copy(overview_src, journey_dir / "overview.md")

        for stage in journey.stages:
            if not stage.components:
                continue
            slug = STAGE_SLUG_MAP.get(stage.stage_name, stage.stage_name.lower().replace(" ", "-"))
            stage_dir = self.docs_dir / slug
            if not stage_dir.exists():
                continue

            nav_lines.append(f"    - '{stage.stage_name}':")
            nav_lines.append(f"      - Overview: {slug}/overview.md")
            if (stage_dir / "components.md").exists():
                nav_lines.append(f"      - Components: {slug}/components.md")
            if (stage_dir / "diagram.md").exists():
                nav_lines.append(f"      - Diagram: {slug}/diagram.md")

        nav_lines.append("")

        # Reference section
        nav_lines.append("  - Reference:")

        ref_files = {
            "hidden-rules-discovered.md": "Hidden Rules",
            "integration-inventory.md": "Integration Inventory",
            "risk-register.md": "Risk Register",
            "00-cross-cutting-components.md": "Cross-Cutting Components",
        }

        # Create reference/ subdirectory with copies
        ref_dir = self.docs_dir / "reference"
        ref_dir.mkdir(exist_ok=True)
        ref_nav_map = {
            "hidden-rules-discovered.md": ("hidden-rules.md", "Hidden Rules"),
            "integration-inventory.md": ("integrations.md", "Integration Inventory"),
            "risk-register.md": ("risk-register.md", "Risk Register"),
            "00-cross-cutting-components.md": ("cross-cutting.md", "Cross-Cutting"),
        }

        for src_name, (dst_name, label) in ref_nav_map.items():
            src = self.docs_dir / src_name
            dst = ref_dir / dst_name
            if src.exists():
                shutil.copy(src, dst)
                nav_lines.append(f"    - '{label}': reference/{dst_name}")

        return "\n".join(nav_lines)

    def _run_mkdocs_build(self) -> Path:
        """
        Run `mkdocs build` to generate the static HTML site.
        Returns path to site/ directory.
        """
        site_path = self.site_root / "site"

        # Check if mkdocs is installed
        if not shutil.which("mkdocs"):
            logger.warning(
                "mkdocs not found in PATH. Skipping HTML build.\n"
                "Install with: pip install mkdocs mkdocs-material pymdown-extensions\n"
                f"Then run: cd {self.site_root} && mkdocs build"
            )
            return site_path

        logger.info(f"  Running: mkdocs build (in {self.site_root})")
        try:
            result = subprocess.run(
                ["mkdocs", "build", "--clean", "--quiet"],
                cwd=str(self.site_root),
                capture_output=True,
                text=True,
                timeout=120,
            )
            if result.returncode == 0:
                logger.info(f"  ✅ MkDocs site built → {site_path}")
            else:
                logger.warning(f"  ⚠️ MkDocs build warnings:\n{result.stderr}")
        except subprocess.TimeoutExpired:
            logger.warning("  MkDocs build timed out")
        except Exception as e:
            logger.warning(f"  MkDocs build error: {e}")

        return site_path

    def serve(self, port: int = 8000) -> None:
        """
        Run `mkdocs serve` for local preview with live reload.
        Blocks until Ctrl+C.
        """
        if not shutil.which("mkdocs"):
            logger.error("mkdocs not installed. Run: pip install mkdocs mkdocs-material")
            return

        print(f"\n🌐 Starting MkDocs server at http://localhost:{port}")
        print("   Press Ctrl+C to stop\n")
        try:
            subprocess.run(
                ["mkdocs", "serve", "--dev-addr", f"localhost:{port}"],
                cwd=str(self.site_root),
            )
        except KeyboardInterrupt:
            print("\nServer stopped.")
