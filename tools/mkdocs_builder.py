"""
tools/mkdocs_builder.py

Builds a complete MkDocs site from the generated documentation.

After DocWriter creates the .md files, this module:
1. Generates mkdocs.yml with Material theme + Mermaid support
2. Builds the navigation tree from the actual generated files
3. Writes a custom CSS overrides file for a professional look
4. Optionally runs `mkdocs build` to create the static HTML site

The output is a self-contained `site/` folder you can:
  - Open locally (just open site/index.html)
  - Push to GitHub Pages
  - Deploy to any static hosting (Netlify, Vercel, S3)
"""
